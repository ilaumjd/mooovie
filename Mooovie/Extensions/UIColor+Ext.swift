import UIKit

extension UIColor {
    
    @nonobjc class var lemonMeringue: UIColor {
        return UIColor(named: "LemonMeringue") ?? .white
    }
    
    @nonobjc class var maximumRed: UIColor {
        return UIColor(named: "MaximumRed") ?? .white
    }
    
    @nonobjc class var maximumYellow: UIColor {
        return UIColor(named: "MaximumYellow") ?? .white
    }
    
    @nonobjc class var orange: UIColor {
        return UIColor(named: "Orange") ?? .white
    }
    
    @nonobjc class var prussianBlue: UIColor {
        return UIColor(named: "PrussianBlue") ?? .white
    }
    
}
